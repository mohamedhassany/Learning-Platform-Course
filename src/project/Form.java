    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here: 
        String oldPassword = new String(txtOldPassword.getPassword());
    String newPassword = new String(txtNewPassword.getPassword());
    
    // 2. تحديد نوع المستخدم من RadioButton
    String role = "";
    if (radiostudentup.isSelected()) {
        role = "Student";
    } else if (radioinstructorsup.isSelected()) {
        role = "Instructor";
    } else {
        JOptionPane.showMessageDialog(this, "Please select your role (Student or Instructor)");
        return;
    }

    // 3. التحقق الأساسي من الحقول
    if (oldPassword.isEmpty() || newPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "يجب إدخال كلمة المرور القديمة والجديدة");
        return;
    }

    try (Connection conn = DriverManager.getConnection(url)) {
        // 4. إنشاء استعلام التحديث
        String tableName = role.equals("Student") ? "Students" : "Instructors";
        String sql = "UPDATE " + tableName + " SET Password = ? WHERE Password = ?";
        
        // 5. تنفيذ الاستعلام
        try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, newPassword); // كلمة المرور الجديدة
            pstmt.setString(2, oldPassword); // كلمة المرور القديمة
            
            int rowsUpdated = pstmt.executeUpdate();
            
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "تم تحديث كلمة المرور بنجاح");
                txtOldPassword.setText("");
                txtNewPassword.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "كلمة المرور القديمة غير صحيحة");
            }
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "خطأ في قاعدة البيانات: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

