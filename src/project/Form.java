    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
String password = new String(txtPassword.getPassword());

String role = "";
if (radioStudent.isSelected()) {
    role = "Student";
} else if (radioInstructor.isSelected()) {
    role = "Instructor";
} else {
    JOptionPane.showMessageDialog(null, "Please select a role (Student or Instructor)");
    return;
}

try (Connection conn = DriverManager.getConnection(url)) {
    String sql;
    if (role.equals("Student")) {
        sql = "SELECT StudentID, FullName, Email FROM Students WHERE user_name = ? AND Password = ?";
    } else {
        sql = "SELECT InstructorID,FullName , Email FROM Instructors WHERE user_name = ? AND Password = ?";
    }

    PreparedStatement ps = conn.prepareStatement(sql);
    ps.setString(1, username);
    ps.setString(2, password);
    
    ResultSet rs = ps.executeQuery();

    if (rs.next()) {
        String fullName = rs.getString("FullName");
        String email = rs.getString("Email");
        JOptionPane.showMessageDialog(null, "Login successful as " + role + 
                                    "\nName: " + fullName + 
                                    "\nEmail: " + email);
    } else {
        JOptionPane.showMessageDialog(null, "Invalid username, password, or role.");
    }

} catch (Exception ex) {
    ex.printStackTrace();
    JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
}
        
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String fullName = txtFullName.getText();
String username = txtUsername2.getText();
String password = new String(txtPassword2.getPassword());
String confirmPassword = new String(txtConfirmPassword.getPassword());
String email = txtEmail.getText();
String IDs = txtIDs.getText();

String role = "";
if (radioStudent2.isSelected()) {
    role = "Student";
} else if (radioInstructor2.isSelected()) {
    role = "Instructor";
} else {
    JOptionPane.showMessageDialog(null, "Please select a role.");
    return;
}

// التحقق من البيانات
if (fullName.isEmpty() || username.isEmpty() || password.isEmpty() || confirmPassword.isEmpty() || email.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Please fill all fields.");
    return;
}

if (!password.equals(confirmPassword)) {
    JOptionPane.showMessageDialog(null, "Passwords do not match.");
    return;
}

try (Connection conn = DriverManager.getConnection(url)) {
    // التحقق من وجود اسم المستخدم مسبقاً
    String checkSql;
    if (role.equals("Student")) {
        checkSql = "SELECT * FROM Students WHERE user_name = ?";
    } else {
        checkSql = "SELECT * FROM Instructors WHERE user_name = ?";
    }
    
    PreparedStatement checkStmt = conn.prepareStatement(checkSql);
    checkStmt.setString(1, username);
    ResultSet rs = checkStmt.executeQuery();

    if (rs.next()) {
        JOptionPane.showMessageDialog(null, "Username already exists.");
        return;
    }

    // إضافة المستخدم الجديد
    String insertSql;
    if (role.equals("Student")) {
        insertSql = "INSERT INTO Students (StudentID,FullName, Email, user_name, Password) VALUES ( ?,?, ?, ?, ?)";
    } else {
        insertSql = "INSERT INTO Instructors (InstructorID,FullName, Email, user_name, Password) VALUES ( ?,?, ?, ?, ?)";
    }
    
    PreparedStatement insertStmt = conn.prepareStatement(insertSql);
    insertStmt.setString(1, IDs);
    insertStmt.setString(2, fullName);
    insertStmt.setString(3, email);
    insertStmt.setString(4, username);
    insertStmt.setString(5, password);
    

    

    int rowsInserted = insertStmt.executeUpdate();

    if (rowsInserted > 0) {
        JOptionPane.showMessageDialog(null, "Registration successful as " + role + "!");
    } else {
        JOptionPane.showMessageDialog(null, "Registration failed. Try again.");
    }

} catch (Exception ex) {
    ex.printStackTrace();
    JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
}
    }//GEN-LAST:event_btnRegisterActionPerformed

